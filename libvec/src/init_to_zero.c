#include "../includes/libvec.h"

void	init_to_zero(t_matrix *m)
{
	int i;
	int j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			m->m[j][i] = 0;
			j++;
		}
		i++;
	}
}
